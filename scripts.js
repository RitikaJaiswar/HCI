//upload Assignment
const uploadForm = document.getElementById('uploadForm');
const uploadedFilesDiv = document.getElementById('uploadedFiles');

uploadForm.addEventListener('submit', function(event) {
    event.preventDefault();
    
    const fileInput = document.getElementById('file');
    const files = fileInput.files;
    
    for (let i = 0; i < files.length; i++) {
        const file = files[i];
        const fileItem = document.createElement('div');
        fileItem.classList.add('file-item');
        fileItem.innerHTML = `<a href="${URL.createObjectURL(file)}" target="_blank">>>${file.name}</a>`;
        uploadedFilesDiv.appendChild(fileItem);
    }
    
    // Clear the file input after uploading
    uploadForm.reset();
});

//post Assignment
// script.js
document.getElementById('postAssignmentForm').addEventListener('submit', function(event) {
    event.preventDefault();
    
    // Get form values
    var title = document.getElementById('title').value;
    var task = document.getElementById('task').value;
    var dueDate = document.getElementById('dueDate').value;

    // Create assignment element
    var assignment = document.createElement('div');
    assignment.classList.add('assignment');
    assignment.innerHTML = `
        <h2>>>Title:${title}</h2>
        <p><strong>Task:</strong> ${task}</p>
        <p><strong>Due Date:</strong> ${dueDate}</p>
    `;

    // Append assignment to assignments container
    document.getElementById('assignments').appendChild(assignment);
    
    // Clear form fields
    document.getElementById('postAssignmentForm').reset();
});

//Add Students

// script.js
document.getElementById('addStudentForm').addEventListener('submit', function(event) {
    event.preventDefault();

    // Get form values
    var name = document.getElementById('name').value;
    var rollNumber = document.getElementById('rollNumber').value;
    var grade = document.getElementById('grade').value;

    // Add student to list
    var studentList = document.getElementById('studentList');
    var studentItem = document.createElement('li');
    studentItem.classList.add('student-item');
    studentItem.innerHTML = `<strong>Name:</strong> ${name}, <strong>Roll Number:</strong> ${rollNumber}, <strong>Div:</strong> ${grade}`;
    studentList.appendChild(studentItem);

    // Clear form fields
    document.getElementById('addStudentForm').reset();
});
